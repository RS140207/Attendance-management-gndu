class Student {
  constructor(id, name, father, class_group_no, lab_group_no) {
    this.id = id;
    this.name = name;
    this.father = father;
    this.class_group_no = class_group_no;
    this.lab_group_no = lab_group_no;
    this.attendance = []; // true = present, false = absent
    this.attendancePercentage = 0;
  }

  markAttendance(isPresent) {
    this.attendance.push(isPresent);
    this.attendancePercentage =
      (this.attendance.filter(Boolean).length / this.attendance.length) * 100;
  }

  getAttendanceColor() {
    if (this.attendancePercentage >= 75) return 'green';
    if (this.attendancePercentage >= 50) return 'yellow';
    return 'red';
  }
}

